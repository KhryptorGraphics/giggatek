    <script>
        document.addEventListener('DOMContentLoaded', function() {
            // Quantity selector
            const quantityInput = document.getElementById('quantity');
            const decreaseBtn = document.getElementById('decrease-quantity');
            const increaseBtn = document.getElementById('increase-quantity');
            
            decreaseBtn.addEventListener('click', function() {
                const currentValue = parseInt(quantityInput.value);
                if (currentValue > 1) {
                    quantityInput.value = currentValue - 1;
                }
            });
            
            increaseBtn.addEventListener('click', function() {
                const currentValue = parseInt(quantityInput.value);
                if (currentValue < 10) {
                    quantityInput.value = currentValue + 1;
                }
            });
            
            // Product gallery thumbnails
            const mainImage = document.getElementById('main-product-image');
            const thumbnails = document.querySelectorAll('.thumbnail');
            
            thumbnails.forEach(thumbnail => {
                thumbnail.addEventListener('click', function() {
                    // Remove active class from all thumbnails
                    thumbnails.forEach(t => t.classList.remove('active'));
                    
                    // Add active class to clicked thumbnail
                    this.classList.add('active');
                    
                    // Update main image
                    const thumbnailImg = this.querySelector('img');
                    mainImage.src = thumbnailImg.src;
                    mainImage.alt = thumbnailImg.alt;
                });
            });
            
            // Product tabs
            const tabLinks = document.querySelectorAll('.product-tabs a');
            const tabPanes = document.querySelectorAll('.tab-pane');
            
            tabLinks.forEach(link => {
                link.addEventListener('click', function(e) {
                    e.preventDefault();
                    
                    // Remove active class from all links and panes
                    tabLinks.forEach(l => l.classList.remove('active'));
                    tabPanes.forEach(p => p.classList.remove('active'));
                    
                    // Add active class to clicked link
                    this.classList.add('active');
                    
                    // Show corresponding tab pane
                    const targetId = this.getAttribute('href').substring(1) + '-pane';
                    document.getElementById(targetId).classList.add('active');
                });
            });
            
            // Rent options
            const rentOptions = document.querySelectorAll('.rent-option');
            
            rentOptions.forEach(option => {
                option.addEventListener('click', function() {
                    // Remove active class from all options
                    rentOptions.forEach(o => o.classList.remove('active'));
                    
                    // Add active class to clicked option
                    this.classList.add('active');
                });
            });
            
            // Add to cart button
            document.getElementById('add-to-cart').addEventListener('click', function() {
                const quantity = parseInt(quantityInput.value);
                alert(`Added ${quantity} NVIDIA GeForce RTX 3080 to cart!`);
            });
            
            // Rent now button
            document.getElementById('rent-now').addEventListener('click', function() {
                const selectedOption = document.querySelector('.rent-option.active');
                const term = selectedOption.dataset.term;
                alert(`Selected ${term}-month rental plan. Redirecting to checkout...`);
            });
        });
    </script>
